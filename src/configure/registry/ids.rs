macro_rules! define_handle {
    ($name:ident) => {
        #[derive(Debug, Clone, PartialEq, Eq, PartialOrd, Ord, Hash)]
        pub struct $name(String);

        impl $name {
            pub fn new(id: impl Into<String>) -> Self {
                Self(id.into())
            }

            pub fn as_str(&self) -> &str {
                &self.0
            }
        }

        impl From<&str> for $name {
            fn from(value: &str) -> Self {
                Self::new(value)
            }
        }

        impl From<String> for $name {
            fn from(value: String) -> Self {
                Self::new(value)
            }
        }

        impl std::fmt::Display for $name {
            fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
                self.0.fmt(f)
            }
        }
    };
}

define_handle!(SettingHandle);
define_handle!(ThemeHandle);
define_handle!(ColorHandle);
define_handle!(SymbolHandle);
define_handle!(CommandHandle);
define_handle!(EventHandle);
define_handle!(MchartFunctionHandle);
define_handle!(ContentModeHandle);
define_handle!(PluginHandle);
